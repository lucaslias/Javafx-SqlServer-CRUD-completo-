package Agenda;

public class InfoDent {
	
	private String idDentista;
	private String cpfDent;
	private String nomeDent;
	private String telefoneDentista;
	private String especialidadeDentista;
	
	public String getIdDentista() {
		return idDentista;
	}
	public void setIdDentista(String idDentista) {
		this.idDentista = idDentista;
	}
	public String getCpfDent() {
		return cpfDent;
	}
	public void setCpfDent(String cpfDent) {
		this.cpfDent = cpfDent;
	}
	public String getNomeDent() {
		return nomeDent;
	}
	public void setNomeDent(String nomeDent) {
		this.nomeDent = nomeDent;
	}
	public String getTelefoneDentista() {
		return telefoneDentista;
	}
	public void setTelefoneDentista(String telefoneDentista) {
		this.telefoneDentista = telefoneDentista;
	}
	public String getEspecialidadeDentista() {
		return especialidadeDentista;
	}
	public void setEspecialidadeDentista(String especialidadeDentista) {
		this.especialidadeDentista = especialidadeDentista;
	}


}


