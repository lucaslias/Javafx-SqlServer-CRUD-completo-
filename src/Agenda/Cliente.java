package Agenda;

public class Cliente {
	
	private String cpf;
	private String nome;
	private String telefone;
	private String enderecoRua;
	private String enderecoNum;
	private String enderecoComp;
	
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEnderecoRua() {
		return enderecoRua;
	}
	public void setEnderecoRua(String enderecoRua) {
		this.enderecoRua = enderecoRua;
	}
	public String getEnderecoNum() {
		return enderecoNum;
	}
	public void setEnderecoNum(String enderecoNum) {
		this.enderecoNum = enderecoNum;
	}	
	public String getEnderecoComp() {
		return enderecoComp;
	}
	public void setEnderecoComp(String enderecoComp) {
		this.enderecoComp = enderecoComp;
	}
	
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}


	

}
